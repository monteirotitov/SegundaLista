
public class exercicioR {
	
	public static void main(String[] args) {

        int fulano = 160;

        int ciclano = 110;

        int i = 0;

        

        while(fulano >= ciclano) {

            fulano += 2;

            ciclano += 3;

            i++;

        }

        

        System.out.println("Ciclano superou a altura de Fulano em " + i + " anos");

    }

}
